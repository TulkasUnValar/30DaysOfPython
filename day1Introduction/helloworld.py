"""Python File
First open your project folder, 30DaysOfPython. If you don't have this folder, create a folder name called 30DaysOfPython. Inside this folder, create a file called helloworld.py. Now, let's do what we did on python interactive shell using visual studio code.

The Python interactive shell was printing without using print but on visual studio code to see our result we should use a built in function _print(). The print() built-in function takes one or more arguments as follows print('argument1', 'argument2', 'argument3'). See the examples below.
"""

print("Hello, World!")  # prints Hello, World!
print(3 + 4)  # prints 7
print(2 * 3)  # prints 6
print(4 + 5 * 2)  # prints 14
print(10 / 2)  # prints 5.0
print(10 // 3)  # prints 3
print(10 % 3)  # prints 1
print(2**3)  # prints 8

# Checking the datatypes
print(type("Hello, World!"))  # prints <class 'str'>
print(type(3 + 4))  # prints <class 'int'>
print(type(11))
print(type(3 * 3j))  # prints <class 'int'>
print(type([1, 2, 3]))  # prints <class 'list'>
print(type({"name": "Tulkas", "country": "Mordorian"}))  # prints <class 'dict'>
print(type(True))  # prints <class 'bool'>
print(type(3.14))  # prints <class 'float'>
print(type(None))  # prints <class 'NoneType'>
print(type(3 + 4.0))  # prints <class 'float'>
print(type({"strength", "intelligence", "wisdom"}))  # prints <class 'set'>
print(type(("Mauricio", "Tulkas")))  # prints <class 'tuple>

